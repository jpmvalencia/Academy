    private void nextButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nextButtonMouseClicked
        // Valida que los campos esten ingresados
        if(userInput.getText().isBlank() || String.valueOf(psswdInput.getPassword()).isBlank() || String.valueOf(rePsswdInput.getPassword()).isBlank()  || emailInput.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Todos los datos deben ser ingresados.");
        } else if(userInput.getText().length() < 2 || userInput.getText().length() > 10) {  // Valida que el usuario este entre 2 y 9 de tama침o
            JOptionPane.showMessageDialog(this, "El usuario debe tener un tama침os de entre 2 y 9 caracteres.");
        } else if(String.valueOf(psswdInput.getPassword()).length() < 8) {  // Valida que la contrase침a tenga m칤nimo 8 caracteres
            JOptionPane.showMessageDialog(this, "La contrase침a debe contener por m칤nimo 8 caracteres.");
        } else if(!String.valueOf(psswdInput.getPassword()).equals(String.valueOf(rePsswdInput.getPassword()))) {   // Valida que la contrase침a est칠 bien escrita
            JOptionPane.showMessageDialog(this, "Las contrase침as no son iguales.");
        } else {    // En caso de que todos los campos est칠n correctos
            
            
            Student estudiante = new Student(this.userInput.getText(), this.nameInput.getText(), this.lastNameInput.getText(), String.valueOf(this.psswdInput.getPassword()), this.emailInput.getText());
            EstudianteDao dao = new EstudianteDao();
            if(dao.agregar(estudiante) != 1) {
                // Limpiar campos
                userInput.setText("");
                nameInput.setText("");
                lastNameInput.setText("");
                emailInput.setText("");
                psswdInput.setText("");
                rePsswdInput.setText("");
                
                
                dao.insertarUsuarioEnDB();
            }
            
            
        }
        

    }//GEN-LAST:event_nextButtonMouseClicked

